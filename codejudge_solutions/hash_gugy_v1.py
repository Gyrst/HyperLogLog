def hex2bin(hex):
    hex_bin_rep_row = []
    decimal = int(hex, 16)
    bin_rep = "{:032b}".format(decimal)
    for bit in bin_rep:
        hex_bin_rep_row.append(int(bit))
    
    return hex_bin_rep_row

